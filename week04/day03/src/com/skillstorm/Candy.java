package com.skillstorm;

public class Candy {
	
	// property
	private double weight;
	
	// constructors
	public Candy() {
		super();
	}
	
	public Candy(double weight) {
		super();
		this.weight = weight;
	}

	// getter/setter
	public double getWeight() {
		return weight;
	}

	public void setWeight(double weight) {
		this.weight = weight;
	}

}
